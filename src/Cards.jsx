import React from 'react';
import { useNavigate } from 'react-router-dom';
import One from './samsunggalaxy.webp';
import Two from './iphonethirteen.jfif';
import Three from './samsungssixty.jfif';
import Four from './motrolla.jfif';
import Five from './iphonrpromax.jfif';
import Six from './iphonrpromax.jfif';
const Cards = () => {
  const navigate = useNavigate();

  const handleNavigateToSamsung = () => {
    navigate('/samsungglaxysix'); // Navigate to the Samsungglaxysix component
  };

  const handleNavigateToIphone = () => {
    navigate('/iphonethirteen'); // Navigate to the Iphonethirteen component
  };

  const handleNavigateToSamsungSixty = () => {
    navigate('/samsungsixty'); // Navigate to the Samsungsixty component
  };

  const handleNavigateToIphoneProMax = () => {
    navigate('/iphonepromax'); // Navigate to the Iphonepromax component
  };

  const handleNavigateToMotrolla = () => {
    navigate('/motrolla'); // Navigate to the Motrolla component
  };

  const handleNavigateToHtc = () => {
    navigate('/htc'); // Navigate to the Htc component
  };

  const handleReturn = () => {
    navigate('/login'); // Navigate back to the Login component
  };

  return (
    <>
      <div className="cardsparent">
        <div className="cardfirstparent">
          <div className="forhoneandcolorline">
            <h1>Card Section</h1>
            <div className="colorline"></div>
            <div className="forreturnone">
              <button className="returnone" onClick={handleReturn}>
                Return to login page
              </button>
            </div>
          </div>
        </div>
        <div className="cardstart">
          <div className="firstcardsection">
            <div
              className="samsungs6oarents6"
              onClick={handleNavigateToSamsung}
              style={{ backgroundImage: `url(${One})` }}
            ></div>
            <div
              className="iphonethirteen"
              onClick={handleNavigateToIphone}
              style={{ backgroundImage: `url(${Two})` }}
            ></div>
            <div
              className="samsungsixty"
              onClick={handleNavigateToSamsungSixty}
              style={{ backgroundImage: `url(${Three})` }}
            ></div>
          </div>
          <div className="firstcardsection">
            <div
              className="motrolla"
              onClick={handleNavigateToMotrolla}
              style={{ backgroundImage: `url(${Four})` }}
            ></div>
            <div
              className="iphonrpromax"
              onClick={handleNavigateToIphoneProMax}
              style={{ backgroundImage: `url(${Five})` }}
            ></div>
            <div
              className="htc"
              onClick={handleNavigateToHtc}
              style={{ backgroundImage: `url(${Six})` }}
            ></div>
          </div>
        </div>
      </div>
    </>
  );
};

export default Cards;
